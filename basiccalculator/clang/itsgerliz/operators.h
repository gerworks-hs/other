#ifndef operators

double add(double num1, double num2) {
	return (num1 + num2);
}

double sub(double num1, double num2) {
	return (num1 - num2);
}

double mult(double num1, double num2) {
	return (num1 * num2);
}

double divi(double num1, double num2) {
	return (num1 / num2);
}

#define operators
#endif
